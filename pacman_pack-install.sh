
PKG_FILE="./packages.txt"

read -p "Start installation progress? [y/n]: " confirm
while [[ "$confirm" != "y" ]]; do
    exit
done

######################################################################################################################################################

if [ ! -f "$PKG_FILE" ]; then
    echo "Ошибка: Файл $PKG_FILE не найден!"
    exit 1
fi

######################################################################################################################################################





######################################################################################################################################################

echo ""
echo "=== sync pacman ==="
echo ""
sudo pacman -Syu --noconfirm

sleep 2

######################################################################################################################################################

echo ""
echo "=== install pacman pack ==="
echo ""
sleep 1

awk '$2=="pacman" {print $1}' "$PKG_FILE" | xargs -r sudo pacman -S --needed --noconfirm
sleep 2

######################################################################################################################################################
